package org.restlet.engine;

import java.util.ArrayList;
import java.util.List;

import org.restlet.Context;
import org.restlet.data.Protocol;

/**
 * Base connector helper.
 * 
 * @author Jerome Louvel
 */
public abstract class ConnectorHelper<T extends Context> extends AbstractHelper<T> {
    /** The protocols simultaneously supported. */
    private List<Protocol> protocols;

    /**
     * Constructor.
     */
    public ConnectorHelper(T connector, List<Protocol> protocols) {
        super(connector);
        this.protocols = protocols;
    }

    @Override
    public Context createContext(String loggerName) {
        return null;
    }

    /**
     * Returns the protocols simultaneously supported.
     * 
     * @return The protocols simultaneously supported.
     */
    public List<Protocol> getProtocols() {
        return this.protocols;
    }

    @Override
    public synchronized void start() throws Exception {
    }

    @Override
    public synchronized void stop() throws Exception {
    }

    @Override
    public void update() throws Exception {
    }

}
